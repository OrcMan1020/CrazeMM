#import "UIFont+Utils.h"

@implementation UIFont (Utils)

+(UIFont*)smallFont
{
    return [UIFont systemFontOfSize:14.f];
}

+(UIFont*)middleFont
{
    return [UIFont systemFontOfSize:16.f];

}

+(UIFont*)largeFont
{
    return [UIFont systemFontOfSize:18.f];
}

@end
